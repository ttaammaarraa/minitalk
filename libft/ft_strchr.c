#include"libft.h"

char	*ft_strchr(const char *s, int c)
{
	char			cc;
	unsigned int	i;

	i = 0;
	cc = (char) c;
	while (s[i] != '\0')
	{
		if (s[i] == cc)
			return ((char *)&s[i]);
		i++;
	}
	if (cc == '\0')
		return ((char *)&s[i]);
	return (NULL);
}
